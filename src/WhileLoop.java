import java.util.Scanner;

public class WhileLoop {
    public static void main(String[] args) {
        int num = 0;

        while (num < 20) {
            System.out.println("moja wartośc to : " + num + " i jestem mniejsza od 20");
            num++;


            while (num < 0) {
                System.out.println("moja wartośc to : " + num + " i jestem mniejsza od 20");
                num++;


            }


        }


    }}
