public class ForLoop {
    public static void main(String[] args) {

        for (int i = 0; i <= 30; i=i+2) {
            System.out.println("Drukuje co 2 " + i);
        }

        for (int j = 1; j <= 50; j++) {
            if (j%5==0)
                System.out.println("j jest podzielne przez " + j);
            }
        }
}

